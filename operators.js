let num1 = 10;
let num2 = 5;

// Arithmetic operators

//multiplication
console.log(num1 * num2);

//Division
console.log(num1 / num2);

//Addition
console.log(num1 + num2);

//Subtraction
console.log(num1 - num2);

//Modulus
console.log(num1 % num2);

//Using Increment and Decrement

//Increment
console.log(++num1);
console.log(++num2);

//Decrement
console.log(--num1);
